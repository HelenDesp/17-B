const speed = 140; // Animation speed in milliseconds
const MAX_HEIGHT = 9;
const MAX_WIDTH = 47;

const AsciiAnimation = () => {
    const [currentFrame, setCurrentFrame] = useState(0);

    // Effect to handle the animation interval
    useEffect(() => {
        const animationInterval = setInterval(() => {
            setCurrentFrame(prev => (prev + 1) % finalFrames.length);
        }, speed);

        return () => clearInterval(animationInterval); // Cleanup on unmount
    }, []);

    // These styles are an exact match of the original HTML file.
    const textareaStyle = {
        fontFamily: "'Courier New', monospace",
        fontSize: '22px',
        fontWeight: 'bold',
        color: '#000000',
        lineHeight: 1.1,
        backgroundColor: 'transparent',
        textAlign: 'center',
        overflow: 'hidden',
        border: 'none',
        resize: 'none',
        outline: 'none',
    };

    return (
        <textarea
            readOnly
            style={textareaStyle}
            rows={MAX_HEIGHT}
            cols={MAX_WIDTH}
            value={finalFrames[currentFrame]}
        />
    );
};

export default AsciiAnimation;